package arrays;

public class rollno {
    public static void main(String[] args) {
        int[] rollNumbers = {101, 102, 103, 104, 105};
        
        // Print the roll numbers
        for (int i = 0; i < rollNumbers.length; i++) {
            System.out.println("Roll Number: " + rollNumbers[i]);
        }
    }
    
}
